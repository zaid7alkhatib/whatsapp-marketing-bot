function getInitialLanguage(): ClientLanguage {
  if (typeof window === "undefined") {
    return "en";
  }

  const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return storedLanguage === "ar" ? "ar" : "en";
}

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, token: string) => {
    const value = params[token];
    return value === undefined ? `{${token}}` : String(value);
  });
}

export function ClientLocaleProvider({ children }: { children: ReactNode }) {
  const { user } = useAuth();
  const [language, setLanguageState] = useState<ClientLanguage>(getInitialLanguage);
  const isClientUser = user?.role === "manager" || user?.role === "viewer";
  const isArabic = language === "ar";
  const isRtl = isArabic;

  const setLanguage = useCallback((nextLanguage: ClientLanguage) => {
    setLanguageState(nextLanguage);
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = isRtl ? "rtl" : "ltr";
  }, [isRtl, language]);

  const value = useMemo<ClientLocaleContextValue>(
    () => ({
      language,
      isArabic,
      isRtl,
      isClientUser,
      setLanguage,
      t: (key, params) =>
        interpolate(translations[language][key] ?? translations.en[key] ?? key, params),
    }),
    [isArabic, isClientUser, isRtl, language, setLanguage]
  );

  return <ClientLocaleContext.Provider value={value}>{children}</ClientLocaleContext.Provider>;
}

const ClientLocaleContext = createContext<ClientLocaleContextValue | undefined>(undefined);

export function useClientLocale(): ClientLocaleContextValue {
  const context = useContext(ClientLocaleContext);

  if (!context) {
    throw new Error("useClientLocale must be used inside ClientLocaleProvider");
  }

  return context;
}
